import type { LessonData } from "./lessons";
import { buildTherapyLesson } from "./ar-therapy-shared";

// دورة "علاج التخاطب — مبنية على المراجع"
// 10 كتب مرجعية × 6 دروس = 60 درسًا. أسلوب مبسّط للأطفال، بأدوات عملية للأهل والأخصائي.

interface BookSeed {
  id: string; // e.g. "ar-sp-1"
  bookTitle: string;
  bookAuthorNote: string;
  levelLabel: string;
  vocab: Array<{ word: string; meaning: string; example: string; emoji: string }>;
  lessons: Array<{
    title: string;
    description: string;
    conceptTitle: string;
    conceptExplanation: string;
    tips: Array<{ sentence: string; note: string }>;
    dialogue: Array<{ speaker: string; text: string }>;
    exam: Array<{ question: string; options: string[]; correct: number }>;
    homework: Array<{ question: string; options: string[]; correct: number }>;
  }>;
}

/**
 * Helper to build all 6 lessons for a book seed.
 * Each lesson receives all 8 vocab words (children learn better with fewer, repeated words).
 */
function buildBook(seed: BookSeed): Record<string, LessonData> {
  const out: Record<string, LessonData> = {};
  seed.lessons.forEach((les, i) => {
    const key = `${seed.id}-${i + 1}`;
    out[key] = buildTherapyLesson({
      levelId: seed.id,
      levelLabel: seed.levelLabel,
      lessonNumber: i + 1,
      title: les.title,
      description: les.description,
      vocab: seed.vocab.slice(0, 8),
      dialogue: les.dialogue,
      concept: {
        title: les.conceptTitle,
        explanation: les.conceptExplanation,
        tips: les.tips,
      },
      exam: les.exam,
      homework: les.homework,
    });
  });
  return out;
}

